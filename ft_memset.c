#include "libft.h"

void	*ft_memset(void *d, int c, size_t n)
{
	char	*dst;
	size_t	i;

	dst = d;
	i = 0;
	while (i < n)
	{
		dst[i] = c;
		i++;
	}
	return (d);
}
